package day01;//声明了一个包叫day01

public class HelloWorld {// 声明类HelloWorld

	// 主方法,为程序的入口
	// 程序的执行从main开始,main结束则程序结束
	public static void main(String[] args) {
		// 打印一句话
		// ctrl+f11运行程序
		System.out.println("hello world");

		// 输出换行
		System.out.println("输出并换行");

		// 输出不换行
		System.out.print("输出");
		System.out.print("不换行");
	}

}
